get1merLine <- function(base){

  if(base=="N"){
    rslt <- "***N*** N                NA                NA                NA 0 0 0"
  } else if(base=="A"){
    rslt <- "***A*** A 0.180869410490589 0.549712641499883 0.183618145069187 1 1 1"
  } else if(base=="C"){
    rslt <- "***C*** C 0.270940636949452 0.261524857575724 1.051666695128940 1 1 1"
  } else if(base=="G"){
    rslt <- "***G*** G 0.969844336945558 0.223648473550234 0.239797676167459 1 1 1"
  } else if(base=="T"){
    rslt <- "***T*** T 0.191967874050329 0.613747774943847 0.167992025665629 1 1 1"
  }
  return(rslt)
  
}
